public class Human {

    String firstName;
    boolean isMale;
    int height;
    int weight;
    int age;

    public int getAge() {
        return age;
    }

    public int getHeight() {
        return height;
    }

    public int getWeight() {
        return weight;
    }

    public String getFirstName() {
        return firstName;
    }

    public boolean inMale() {
        return isMale;
    }
}
